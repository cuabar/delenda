		<a id="started"><h3>Getting Started</h3></a>
		<p>Upon registering and signing in, you'll be greeted with the main control page for your command outpost. This page has all the most important information available to
		be easily read.</p><br>
		<center><img style="border:2px solid black" src="../guideimages/main.PNG" width="480px" height="320px" /></center><br>
		<p>Your first step is to begin building residential districts for your outpost. These are the most important buildings in your outpost. They house your logistical personnel and their families,
		and provide you your main source of income (though not your strongest). Hover over the "Management" tab, and scroll to "Construction". Notice that there are in total 5 district types available to be built.
		All of these are equally important to your outpost's growth. Primarily, you need to keep an eye on your Educational, Healthcare and Law districts. Without these, your citizens
		will become unhappy. And unhappy citizens will refuse to work, losing you income.<br><br>
		</p>
		<ul>
		<li><b>+</b> Residential Districts house 10,000 personnel each.<br></li>
		<li><b>+</b> Educational Districts provide education for your population. Each district covers 12,000 civilians.<br></li>
		<li><b>+</b> Healthcare provides medical aid for up to 15,000 civilians per district. Similarly, Law districts provide safety and security for 15,000 civilians.<br></li>
		<li><b>+</b> Commercial Districts offer the greatest source of income for your nation. You can only have 1 Commerce District for every 2 Residential Districts, but they
		provide an untaxed income of 7,500 per district.<br></li>
		</ul>
		<p><br>For the first few game Ticks you will want to focus entirely on building up your population. Once you have a sizeable enough income, you can start to focus on other aspects of
		managing your Outpost.</p><a href="#top"> Go to Top</a><br><br>
		<a id="army"><h3>Building An Army</h3></a>
		<p>There are three ways to build up your military might. 'Troops', Special Forces, and Superweapons.<br><br>Troops are the standard infantry, recruits and war machines from all across your
		faction's empire. It's their job to do the heavy lifting, and fight on the frontlines. Troops are built and deployed in their thousands, and die in equally large numbers. It's
		best not to get too attached to them. Troops are trained by the Training Grounds. Every Training Ground built will increase the number of troops you train per tick. However, there
		is a limit on how many troops your Outpost can handle at any one time. Building more Training Grounds, or Barracks' will increase your maximum amount.<br>It should be noted, the
		Solidarity has a far more vast population to call on for war. Their Troops are trained faster than the other factions and their buildings are prepared for this. Their Barracks
		will therefore be able to contain a greater number of troops than the other factions'.<br><br>
		Special Forces are trained in War Centres. Special Forces are highly trained individuals, and can be trained to use one of several "Kits". Each kit is specialised for a particular
		mission type. You don't send a heavy infantry unit to assassinate an enemy general after all. Operations Training occurs in the "Training" section. At this point in time, there is no
		use for Recon or Black Ops kits. So don't bother training them.<br><br>
		Superweapons are the end game product of a highly developed Outpost. They cost a large sum of money to build but can be very powerful when used in the right situation.
		Superweapons are built by War Factories, and take 6 ticks to build. They are broadly classified as Bombs (which destroy enemy troops) and Shields (which protect your own troops from
		being destroyed by enemy Bombs). On average, the Authority has much more powerful weapon strikes while the Union has the weakest bomb type. All factions have identical shield strength.
		Defensive technology has not advanced far enough to survive a direct hit from a thermonuclear (or worse) weapon.<br><br>
		Furthermore, you need to maintain your Logistical Chain. Building Command Centres provide you with Logistical Points. Each command centre provides 10 LPs per tick. These
		do not stack. If you do not use them by the end of the tick, they are wasted. Make sure to not build more Command centres than you absolutely need!</p><a href="#top"> Go to Top</a><br><br>
		<a id="combat"><h3>Joining the War Effort</h3></a>
		<p>Your army is deployed through the Star Map. The colonised area of the galaxy is broken down into three "Border Sectors" and three "Home Sectors".<br><br>
		Select the sector you wish to deploy in, then find a battlezone where your help is needed. Every Sector has ten battlezones. A faction is destroyed when they control no more battlezones.
		Battlezones represent entire star systems. Control of these systems is gained only through an incredible amount of destruction and blood loss. This is charted by the "Influence"
		value for each battlezone. As your troops win the fight, your faction will gain Influence over a system. The faction with the most Influence is said to be in control of that system.
		<br><br>
		You cannot, however, simply deploy your armies directly to your enemy's homeworld. The road to victory is long and difficult. To deploy forces in a system, your faction must
		control that system's neighbour in the Gate Network. In addition, you cannot deploy forces into an opponent's home sector until you control - <strong>completely</strong> -
		a sector bordering the target Home Sector. The Solidarity, for example, must capture every Battlezone in the Erebus Sector before they can deploy their armies into the Centauri
		Sector.
		<br><br>
		As you gain more and more planets, your faction will become richer for the resources fed into their empire. This results in a minute increase in your factions Affluence value.
		Higher Affluence means a higher tax income for every Outpost in that faction. To offset this, factions which lose ground will become desperate. Experimental weapons will be
		authorised, their troops will fight harder, more unforgiving tactics will be employed. This increase in desperation boosts the troop effectiveness of the faction. Losing more systems
		will result in a higher desperation effect.</p>
		<a href="#top"> Go to Top</a><br><br>
		<a id="research"><h3>Research</h3></a>
		<p>You can contribute to your faction's progress through indirect methods as well. By building Research Complexes, you can accrue Research Points. These represent man hours
		put into research by your Outpost's scientists and engineers. More Research Points basically means your scientists have access to more resources and funding for their
		projects. As they are spent, your scientists will have to start fundraising and collecting needed materials again.<br><br>
		Every faction has a unique tech tree. By investing money, resources and research points into these techs you can assist your faction in various ways. Either by providing 
		a burst of resources available on the Markets, or increasing the effectiveness of your troops. Some technologies are even more unique or interesting than simple boosts to basic stats. For example, the
		Authority has access to Self Repair systems, allowing a small percentage of their Drone troops to recover from their destruction and continue the fight.<br><br>
		Research Trees are not complete, but the basic guideline to each tree is as follows: <br>The Solidarity research increases the brute force of their armies. Increased training rates,
		faster influence accruement, free superweapons for all Outposts are all potential technologies for them.<br>
		The Mercantile Union focuses instead on bolstering their economy. Increasing the effectiveness of extractors, reducing the maintenance cost of structurs and slowing their loss
		of influence on controlled battlezones are all examples.<br>
		The Authority is the more insidious of the factions, focusing on area denial to make up for their critically small armies. Drones self repair, Nanovore swarms slaughter enemy forces while they're vulnerable during deployment, invasive AIs shut down gate networks cutting off enemies from
		retreat or reinforcements....</p>
		<a href="#top"> Go to Top</a><br><br>
		<a id="resource"><h3>Resources</h3></a>
		<p>Ever Outpost has access to one type of resource (Mercantile Outposts have access to two). Building extractors will allow you to mine resources, either for your own use or for 
		selling on the Market to the rest of your faction. Each Extractor will provide 10 of your Outpost's resource every Tick. However, you will lose one of <strong>every</strong> resource in your
		stockpile for every 10,000 civilians in your Outpost. Each resource provides a certain bonus so long as you have at least 1 of it in your stockpile at the beginning of the Tick.
		<br><br>
		<b>Coal</b> decreases Spec Ops training costs by 2% (not yet implemented).<br>
		<b>Copper</b> decreases construction costs by 1%.<br>
		<b>Diamonds</b> increase population happiness by 2%.<br>
		<b>Gold</b> increases the income of Commercial Districts by 1%.<br>
		<b>Livestock</b> decreases residential district construction by 1%.<br>
		<b>Oil</b> decreases Spec Ops upkeep by 5% (not yet implemented, nor is Spec Ops upkeep).<br>
		<b>Palladium</b> increases research point acquisition by 1%.<br>
		<b>Silver</b> increases tax income by 1%.<br>
		<b>Tungsten</b> increases troop training rates by 1%.<br>
		<b>Illucite</b> is very rare, and increases research points acquisition by 10% and total income (taxes+commerce) by 5%.<br></p>
